const cultures = [
    "Nordic", "Slavic", "Celtic", "Elven"
]

function randomChoice(array) {
    return array[Math.floor(Math.random() * array.length)]
}

export class NameGenerator {
    constructor() {

    }

    generate(): string {
        let culture = randomChoice(cultures);
        let prefix = "";
        let first_name = "";
        let last_name = "";

        if (culture === "Nordic") {
            first_name = randomChoice(Math.random() < 0.5 ? nordic_male : nordic_female);
            last_name = randomChoice(nordic_family);
            if (Math.random() < 0.2) {
                prefix = "von ";
            }
        } else if (culture === "Slavic") {
            first_name = randomChoice(Math.random() < 0.5 ? slavic_male : slavic_female);
            last_name = randomChoice(slavic_family);
            if (Math.random() < 0.2) {
                prefix = "of ";
            }
        } else if (culture === "Celtic") {
            first_name = randomChoice(Math.random() < 0.5 ? celtic_male : celtic_female);
            last_name = randomChoice(celtic_family);
            if (Math.random() < 0.2) {
                prefix = randomChoice(["Mac ", "O'"]);
            }
        } else if (culture === "Elven") {
            first_name = randomChoice(Math.random() < 0.5 ? elven_male : elven_female);
            last_name = randomChoice(elven_family);
            if (Math.random() < 0.2) {
                prefix = "de la ";
            }
        }

        return `${first_name} ${prefix}${last_name}`;
    }
}